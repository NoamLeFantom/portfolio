# Fonction pour transformer le JSON
def transform_json(input_data):
    output = {"nodes": [], "links": []}
    group_id = 1  # Initialise l'ID de groupe
    
    for project in input_data["children"]:
        # Ajouter le groupe (SAE)
        group_name = project["matiere"]
        output["nodes"].append({"id": group_name, "group": group_id})
        
        # Ajouter le projet (nom)
        project_name = project["nom"]
        output["nodes"].append({"id": project_name, "group": group_id})
        
        # Lier le projet au groupe
        output["links"].append({"source": project_name, "target": group_name, "value": 1})
        
        # Ajouter les mots-clés enfants et les lier au projet
        for child in project["children"]:
            keyword = child["name"]
            output["nodes"].append({"id": keyword, "group": group_id})
            output["links"].append({"source": keyword, "target": project_name, "value": 1})
        
        group_id += 1  # Passer au groupe suivant
    
    return output

